//! # cr-web
//!
//! Presentation layer for the Czech Republic portal.
//!
//! Axum web server with Askama SSR templates.
//! Handles HTTP routing, request/response DTOs, and HTML rendering.
//!
//! ## Modules (planned)
//!
//! - `routes` - Axum route handlers
//! - `templates` - Askama HTML templates
//! - `error` - HTTP error responses
//! - `state` - Application state (DB pool, config)
